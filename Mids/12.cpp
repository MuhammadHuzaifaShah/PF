#include <iostream>
using namespace std;
 
int main()
{
    auto num1 = 0xA5; // hexadecimal literal
    auto num2 = 045;  // octal literal
    auto X = 5;       // decimal literal
    cout << "The value of num1 is : " << num1 << endl;
    cout << "The value of num2 is : " << num2 << endl;
    cout << "The value of X is : " << X << endl;
   return 0;
}